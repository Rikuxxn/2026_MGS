//===================================================
void CTitle::Update(void)
{
#ifdef _DEBUG
	// マネージャーの取得
	CManager* pManager = CManager::GetInstance();

	// キーボードの取得
	CInputKeyboard* pKeyboard = pManager->GetInputKeyboard();

	if (pKeyboard->GetTrigger(DIK_F9))
	{
		pManager->BeginFade(std::make_unique<CGame>());
	}
#endif // _DEBUG
}

